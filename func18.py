num = lambda q: q.replace('.', '', 1).isdigit()
print(num('26587'))
print(num('4.2365'))
print(num('-12547'))
print(num('00'))
print(num('A001'))
print(num('001'))
print("\nPrint checking numbers:")
num1 = lambda r: num(r[1:]) if r[0] == '-' else num(r)
print(num1('-16.4'))
print(num1('-24587.11'))
